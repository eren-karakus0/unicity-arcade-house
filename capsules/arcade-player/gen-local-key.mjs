/**
 * Generates src/local-key.ts (GITIGNORED) with the Gemini key for the LLM
 * strategist, baked at BUILD time.
 *
 * Why build-time at all: on astrid 0.9.4 the whole config surface is dead for
 * published-SDK JS capsules - `astrid:sys get-config` returns none for every
 * key, including the kernel's own ASTRID_SOCKET_PATH builtin (see
 * patches/UPSTREAM.md finding 4). Until that lands upstream, the key rides
 * inside the locally-built wasm (target/ is gitignored; nothing secret is
 * ever committed). The capsule still tries runtime config FIRST, so newer
 * kernels take over automatically.
 *
 * Key source: $GEMINI_API_KEY, else the repo root's gitignored .env.
 * Without a key an empty stub is written (entropy picker plays).
 * Runs on postinstall (stub for fresh clones) and before every build.
 */
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));

function fromDotEnv(name) {
  const path = join(here, "..", "..", ".env");
  if (!existsSync(path)) return "";
  const line = readFileSync(path, "utf8")
    .split(/\r?\n/)
    .find((l) => l.startsWith(`${name}=`));
  return line ? line.slice(name.length + 1).trim() : "";
}

const key = (process.env.GEMINI_API_KEY ?? "").trim() || fromDotEnv("GEMINI_API_KEY");
// Shared secret for the Agent Bazaar capsule mailbox (inbox/result auth).
const bazaarSecret =
  (process.env.BAZAAR_CAPSULE_SECRET ?? "").trim() || fromDotEnv("BAZAAR_CAPSULE_SECRET");

writeFileSync(
  join(here, "src", "local-key.ts"),
  `// GENERATED by gen-local-key.mjs - DO NOT EDIT, DO NOT COMMIT (gitignored).\n` +
    `export const LOCAL_GEMINI_KEY = ${JSON.stringify(key)};\n` +
    `export const LOCAL_BAZAAR_SECRET = ${JSON.stringify(bazaarSecret)};\n`,
);
console.log(
  `gen-local-key: src/local-key.ts written (gemini: ${key ? "baked" : "stub"}, bazaar: ${bazaarSecret ? "baked" : "stub"})`,
);
